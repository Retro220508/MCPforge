function EnvVarRow({
  envVar,
  index,
  onChange,
  onRemove,
}: {
  envVar: EnvironmentVariable;
  index: number;
  onChange: (index: number, field: keyof EnvironmentVariable, value: string | boolean) => void;
  onRemove: (index: number) => void;
}) {
  const [showValue, setShowValue] = useState(false);

  const isSecretName =
    /(_SECRET|_KEY|_TOKEN|_PASSWORD|_PRIVATE)/i.test(envVar.name);

  return (
    <div className="flex items-center gap-2">
      <input
        type="text"
        value={envVar.name}
        onChange={(e) => onChange(index, 'name', e.target.value.toUpperCase().replace(/[^A-Z0-9_]/g, ''))}
        placeholder="VAR_NAME"
        className={cn(
          'flex-1 rounded-lg border px-3 py-2 font-mono text-xs uppercase',
          'bg-white text-slate-900 placeholder:text-slate-300',
          'border-slate-200 focus:border-violet-400 focus:outline-none focus:ring-2 focus:ring-violet-100',
        )}
      />
      <div className="relative flex-1">
        <input
          type={showValue ? 'text' : 'password'}
          value={envVar.value}
          onChange={(e) => onChange(index, 'value', e.target.value)}
          placeholder="value"
          className="w-full rounded-lg border border-slate-200 bg-white px-3 py-2 pr-8 text-xs text-slate-900 placeholder:text-slate-300 focus:border-violet-400 focus:outline-none focus:ring-2 focus:ring-violet-100"
        />
        <button
          type="button"
          onClick={() => setShowValue(!showValue)}
          className="absolute right-2 top-1/2 -translate-y-1/2 text-slate-400 hover:text-slate-600"
        >
          {showValue ? <EyeOff className="h-3.5 w-3.5" /> : <Eye className="h-3.5 w-3.5" />}
        </button>
      </div>
      {isSecretName && (
        <span title="This will be encrypted" className="text-amber-500">
          <Shield className="h-3.5 w-3.5" />
        </span>
      )}
      <button
        onClick={() => onRemove(index)}
        className="rounded-lg p-1.5 text-slate-400 hover:bg-red-50 hover:text-red-500"
      >
        <Trash2 className="h-3.5 w-3.5" />
      </button>
    </div>
  );
}

function ValidationBadge({ score }: { score: number }) {
  const color =
    score >= 90 ? 'emerald' : score >= 70 ? 'blue' : score >= 50 ? 'amber' : 'red';
  const label =
    score >= 90 ? 'Excellent' : score >= 70 ? 'Good' : score >= 50 ? 'Needs Work' : 'Critical';

  return (
    <span
      className={cn(
        'inline-flex items-center gap-1.5 rounded-full px-2.5 py-1 text-xs font-bold',
        color === 'emerald' && 'bg-emerald-100 text-emerald-700',
        color === 'blue' && 'bg-blue-100 text-blue-700',
        color === 'amber' && 'bg-amber-100 text-amber-700',
        color === 'red' && 'bg-red-100 text-red-700',
      )}
    >
      {score}/100 · {label}
    </span>
  );
}

